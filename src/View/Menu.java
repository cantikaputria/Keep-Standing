private String cekUsername() {

        thl.prosesDataSkor(); // b

        int i = 0, cek = 0;
        while ((cek == 0) && (i < thl.getSize())) {
            if (this.nama.equals(thl.getUsername(i))) {
                cek = 1;
            }
            i++;
        }

        if (cek == 1) {
            fail = Integer.parseInt(thl.getScore(i - 1));
            success = Integer.parseInt(thl.getStanding(i - 1));
            return thl.getId(i - 1);
        } else {
            fail = 0;
            success = 0;
            return "-1";
        }
    }

